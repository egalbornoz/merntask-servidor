/********************************************************************************
 *  Importaciones
 ********************************************************************************/
 const { validationResult } = require('express-validator');
 /********************************************************************************
  *  Middlewares para validar campos
  ********************************************************************************/
 const validarCampos = (req, res, next) => {
     const errors = validationResult(req);
     if (!errors.isEmpty()) {
         return res.status(400).json(errors);
     }
     next();
 }
 /********************************************************************************
  *  Exportaciones
  ********************************************************************************/
 
 module.exports = {
     validarCampos,
 }